import mongoose from "mongoose";

const USERS_COLLECTION = "users";

const userSchema = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const UserModel = mongoose.model(USERS_COLLECTION, userSchema);

export default UserModel;

export { USERS_COLLECTION };
